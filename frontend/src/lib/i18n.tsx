type Translations = typeof translations.en;
type TranslationKeys = keyof Translations;

interface LanguageContextType {
 language: Language;
 setLanguage: (lang: Language) => void;
 t: (key: TranslationKeys) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
 const [language, setLanguageState] = useState<Language>("en");
 const [mounted, setMounted] = useState(false);

 useEffect(() => {
  // Check localStorage on mount
  let savedLang = localStorage.getItem("tubeai_language") as Language | null;
  if (savedLang && (savedLang ==="en"|| savedLang ==="fr"|| savedLang ==="es")) {
   setLanguageState(savedLang);
  } else {
   const browserLang = navigator.language.split("-")[0];
   if (browserLang ==="fr"|| browserLang ==="es") {
    savedLang = browserLang;
    setLanguageState(savedLang);
    localStorage.setItem("tubeai_language", savedLang);
   } else {
    setLanguageState("en");
    localStorage.setItem("tubeai_language","en");
   }
  }
  setMounted(true);
 }, []);

 const setLanguage = (lang: Language) => {
  setLanguageState(lang);
  localStorage.setItem("tubeai_language", lang);
 };

 const t = (key: TranslationKeys): string => {
  return translations[language][key] || translations["en"][key] || key;
 };

 if (!mounted) {
   // During SSR and first client render, use default "en" to match server
   return (
     <LanguageContext.Provider value={{ language:"en", setLanguage, t: (key) => translations["en"][key] || key }}>
      <div style={{ visibility:"hidden"}}>{children}</div>
     </LanguageContext.Provider>
   );
 }

 return (
  <LanguageContext.Provider value={{ language, setLanguage, t }}>
   {children}
  </LanguageContext.Provider>
 );
};

export const useLanguage = () => {
 const context = useContext(LanguageContext);
 if (context === undefined) {
  throw new Error("useLanguage must be used within a LanguageProvider");
 }
 return context;
};
